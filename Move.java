// Move.java
public class Move {
    public int sourceRow, sourceCol;
    public int destRow, destCol;

    public Move(int srcRow, int srcCol, int dstRow, int dstCol) {
        this.sourceRow = srcRow;
        this.sourceCol = srcCol;
        this.destRow = dstRow;
        this.destCol = dstCol;
    }
}
